CREATE TABLE utenti (
    id_utente INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(128),
    cognome VARCHAR(128),
    immprofilo VARCHAR(128),
    descrizione VARCHAR (256),
    datanascita VARCHAR(20),
    password VARCHAR(32),
    sesso VARCHAR(10),
    email VARCHAR(128)
);


CREATE TABLE post (
    id_post INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FOREIGN KEY (utente) REFERENCES utenti(id_utente) ON DELETE CASCADE,
    post VARCHAR(256),
    idUtenteBacheca INTEGER,
    FOREIGN KEY (idUtenteBacheca) REFERENCES utenti(id_utente) ON DELETE CASCADE,
    link_post VARCHAR(256)
);

CREATE TABLE gruppo (
    id_gruppo INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(64),
    descrizione VARCHAR(256),
    FOREIGN KEY (proputente) REFERENCES utenti(id_utente) ON DELETE CASCADE
);


